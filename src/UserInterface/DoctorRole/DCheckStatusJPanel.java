    private void searchtjButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchtjButton3ActionPerformed
        // TODO add your handling code here:
        int searchid = Integer.valueOf(searchjTextField1.getText());

        WorkQueue workQueue = ua.getWorkQueue();
        WorkRequest workRequest = workQueue.searchByID(searchid, ua, org);
        if (workRequest != null) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            Object[] row = new Object[3];
            row[0] = workRequest;
            row[1] = workRequest.getStatus();
            row[2] = workRequest.getLevel1();
            model.addRow(row);
        } else {
            JOptionPane.showMessageDialog(null, "NOT FOUND");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_searchtjButton3ActionPerformed


