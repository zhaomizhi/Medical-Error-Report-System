    private void viewrespondjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewrespondjButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select one row");
        } else {
            WorkRequest workRequest = (WorkRequest) jTable1.getValueAt(selectedRow, 0);
            if (workRequest.getRespond() != null) {
                resjTextArea1.setText(workRequest.getRespond());
            } else {
                resjTextArea1.setText("The Patient service center has not view the request yet");
            }
        }

    }//GEN-LAST:event_viewrespondjButton1ActionPerformed

