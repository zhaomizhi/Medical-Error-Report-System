    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        
        for(RegisterReport registerReport:ua.getRegisterReportDirectory().getRegisterReportList()){
            Object[] row = new Object[4];
            row[0] = registerReport;
           // row[1] = registerReport.getName();
            row[1] = registerReport.getNetwork();
            row[2] = registerReport.getEnterprise();
            row[3] = registerReport.getOrganization();
            
             model.addRow(row);
            
        }
    }
    

