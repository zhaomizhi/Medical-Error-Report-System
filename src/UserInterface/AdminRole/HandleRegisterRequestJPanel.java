    private void approvejButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approvejButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select one row");
        }else{
            RegisterReport registerReport = (RegisterReport) jTable1.getValueAt(selectedRow, 0);
            Organization userOrganization = registerReport.getOrganization();
            UserAccount uaa = userOrganization.getUserAccountDirectory().createUserAccount();
            uaa.setUserName(registerReport.getUsername());
            uaa.setPassword(registerReport.getPassword());
            uaa.setRole(registerReport.getRole());
            
            if(uaa.getRole().toString().equals("Business.Role.PatientRole")){
                uaa.getPatient().setPatientname(registerReport.getRegisterpatient().getPatientname());    
            }if(uaa.getRole().toString().equals("Business.Role.DoctorRole")){
                uaa.getDoctor().setDoctorname(registerReport.getRegisterdoctor().getDoctorname());
                
            }
            
        }
         
        
    }//GEN-LAST:event_approvejButton1ActionPerformed


