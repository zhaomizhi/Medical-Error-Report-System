    private void populateDetail() {
        charajComboBox1.removeAllItems();
        String type = (String) eventjComboBox1.getSelectedItem();
        if (type.equals(Surgery.Surgery)) {
            charajComboBox1.addItem(Surgery.SURGICAL_SITE_INFECTION);
            charajComboBox1.addItem(Surgery.BURN_OPERATING_ROOM_FIRE);
            charajComboBox1.addItem(Surgery.INCORRECT_SURGICAL_PROCEDURE);
            charajComboBox1.addItem(Surgery.LATROGENIC_PNEUMOTHORAX);

        }
        if (type.equals(Surgery.Anesthesia)) {
            charajComboBox1.addItem(Surgery.DENTAL_INJURY);
            charajComboBox1.addItem(Surgery.OCULAR_INJURY);
            charajComboBox1.addItem(Surgery.PERIPHERAL_NERVE_INJURY);
            charajComboBox1.addItem(Surgery.MALIGNANT_HYPERTHERMIA);
        }
    }

    private void populateEvent() {
        eventjComboBox1.removeAllItems();
        eventjComboBox1.addItem(Surgery.Anesthesia);
        eventjComboBox1.addItem(Surgery.Surgery);
    }

