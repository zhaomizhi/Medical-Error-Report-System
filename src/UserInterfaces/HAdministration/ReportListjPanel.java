    private void viewdetailjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewdetailjButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select one row");
        } else {
            WorkRequest workRequest = (WorkRequest) jTable1.getValueAt(selectedRow, 0);
            generaljTextField8.setText(workRequest.getLevel1());
            specificjTextField6.setText(workRequest.getLevel2());
            dateoferrorjTextField2.setText(workRequest.getDateoferror().toString());
            if (workRequest.getSatisfylevel() != null) {
                if (workRequest.getSatisfylevel().equals("YES")) {
                    satijTextField9.setText(workRequest.getSatisfylevel());

                } else {
                    satijTextField9.setText(workRequest.getSatisfylevel());
                    satijTextField9.setForeground(Color.red);
                }

            } else {
                satijTextField9.setText("NOT EVALUTATED");
            }

            sourcejTextField1.setText(workRequest.getSource());
            jTextField1.setText(workRequest.getLocation());
           // level2jTextField1.setText(workRequest.getLevel2());

            DefaultTableModel model = (DefaultTableModel) errorpersonjTable2.getModel();
            model.setRowCount(0);
            for (ErrorPerson errorPerson : workRequest.getEpd().getErrorpersonlist()) {
                Object[] row = new Object[1];
                row[0] = errorPerson;
                model.addRow(row);

            }
        }
        //personneljTextField7.setText(workRequest.getPersonnel());
    }//GEN-LAST:event_viewdetailjButton2ActionPerformed

