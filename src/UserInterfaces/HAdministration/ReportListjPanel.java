    private void populateCombox() {
        for (Network n : es.getNetworkList()) {
            for (Enterprise enter : n.getEd().getEnterprisesList()) {
                if (enter.equals(enterprise)) {
                    for (Enterprise enterpriss : n.getEd().getEnterprisesList()) {
                        if (enterpriss instanceof HospitalEnterprise || enterpriss instanceof ClinicEnterprise) {
                            enterprisejComboBox1.addItem(enterpriss);
                        } else {
                          return;
                        }
                    }
                }
            }
        }
    }

    private void populateEnterpriseTable(Enterprise e) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (WorkRequest workRequest : e.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[3];
            row[0] = workRequest;
            row[1] = workRequest.getSenderEnterprise();
            row[2] = workRequest.getLevel1();
            model.addRow(row);

        }

    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (WorkRequest workRequest : organization.getWorkQueue().getWorkRequestList()) {
            if (workRequest.getInvestigationresule().equals("app")) {
                Object[] row = new Object[3];
                row[0] = workRequest;
                row[1] = workRequest.getSenderEnterprise();
                row[2] = workRequest.getLevel2();
                model.addRow(row);
            }

        }

    }

